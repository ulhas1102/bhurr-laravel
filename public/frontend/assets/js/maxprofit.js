function maxProfit(prices) {
    if (prices.length === 0) {
        return 0;
    }
    let minStockPrice = prices[0] || 0;
    let profit = 0;

    for (let i = 1; i < prices.length; i++) {
        if (prices[i] < minStockPrice) {
            minStockPrice = prices[i];
        }
        profit = Math.max(profit, prices[i] - minStockPrice);
    }
    return profit;
}
